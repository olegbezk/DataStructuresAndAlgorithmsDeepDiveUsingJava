package com.coding.interview.algorithms;

public class Arrays {

    public static void main(String[] args) {
        int[] array = new int[7];

        array[0] = 20;
        array[1] = 25;
        array[2] = -15;
        array[3] = 7;
        array[4] = 55;
        array[5] = 1;
        array[6] = -22;

        for (int j : array) {
            System.out.print(j + " ");
        }
    }
}
